class Date
  include DateExtensions
end

DATE_FORMATS = {
  '%m/%d/%Y %I:%M %p' => /^(0[1-9]|1[012])[\/](0[1-9]|[12][0-9]|3[01])[\/][0-9]{4}$/,
  '%d/%m/%Y %I:%M %p' => /^(0[1-9]|[12][0-9]|3[01])[\/](0[1-9]|1[012])[\/][0-9]{4}$/,
  '%Y/%m/%d %I:%M %p' => /^[0-9]{4}[\/](0[1-9]|1[012])[\/](0[1-9]|[12][0-9]|3[01])$/,
  '%b-%d-%Y' => /^([a-z]|[A-Z]){1,3}[-](0[1-9]|[12][0-9]|3[01])[-][0-9]{4}$/
}
